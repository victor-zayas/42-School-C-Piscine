#include <unistd.h>

void	ft_print_program_name(char *str)
{
	int	i;

	i = 0;
	while (str[i] != '\0')
	{
		write(1, &str[i], 1);
		str++;
	}
	write(1, "\n", 1);
}

int	main(int argc __attribute__((unused)), char **argv)
{
	ft_print_program_name(argv[0]);
}
